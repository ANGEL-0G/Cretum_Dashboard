let LANG = 'es';
try { LANG = localStorage.getItem('lang') === 'en' ? 'en' : 'es'; } catch (e) {}

/** Traduce un string de origen (español). Interpola {vars} opcionales. */
function t(es, vars) {
  let s = (LANG === 'en' && EN[es] != null) ? EN[es] : es;
  if (vars) s = String(s).replace(/\{(\w+)\}/g, (m, k) => (vars[k] != null ? vars[k] : m));
  return s;
}

function currentLang() { return LANG; }

// Guarda los valores originales (español) por elemento, para poder volver a ES.
const _orig = new WeakMap();
function _cap(el) {
  if (!_orig.has(el)) {
    _orig.set(el, {
      text: el.textContent,
      ph: el.getAttribute('placeholder'),
      title: el.getAttribute('title'),
      aria: el.getAttribute('aria-label'),
    });
  }
  return _orig.get(el);
}
function _pick(es) {
  if (es == null) return es;
  const k = String(es).trim();
  return (LANG === 'en' && EN[k] != null) ? EN[k] : es;
}

/* ── Auto-traducción de texto NO marcado (retrofit masivo) ──────────────────
   Compara el texto renderizado contra el diccionario y lo traduce en vivo,
   incluso lo pintado dinámicamente. Guarda el original (ES) para poder volver. */
const _tnOrig = new WeakMap();   // textNode -> valor original (ES)
const _atOrig = new WeakMap();   // element  -> {ph,ti,ar} originales (ES)

function _applyText(node) {
  const cur = node.nodeValue;
  if (cur == null) return;
  if (!_tnOrig.has(node)) {
    if (!(cur.trim() in EN)) return;   // no traducible → ni lo rastreamos
    _tnOrig.set(node, cur);
  }
  const orig = _tnOrig.get(node);
  const key = orig.trim();
  if (LANG === 'en' && EN[key] != null) {
    if (node.nodeValue !== orig.replace(key, () => EN[key])) node.nodeValue = orig.replace(key, () => EN[key]);
  } else if (node.nodeValue !== orig) {
    node.nodeValue = orig;             // volver a español
  }
}

function _applyAttrs(el) {
  if (!el || el.nodeType !== 1) return;
  let o = _atOrig.get(el);
  if (!o) {
    const ph = el.getAttribute('placeholder'), ti = el.getAttribute('title'), ar = el.getAttribute('aria-label');
    const hit = (v) => v != null && v.trim() in EN;
    if (!(hit(ph) || hit(ti) || hit(ar))) return;
    o = { ph, ti, ar }; _atOrig.set(el, o);
  }
  const conv = (v) => (v != null && LANG === 'en' && EN[v.trim()] != null) ? EN[v.trim()] : v;
  if (o.ph != null) el.setAttribute('placeholder', conv(o.ph));
  if (o.ti != null) el.setAttribute('title', conv(o.ti));
  if (o.ar != null) el.setAttribute('aria-label', conv(o.ar));
}

function _autoWalk(root) {
  try {
    if (!root) return;
    if (root.nodeType === 3) { _applyText(root); return; }
    if (root.nodeType !== 1 && root.nodeType !== 9) return;
    const w = document.createTreeWalker(root, NodeFilter.SHOW_TEXT);
    const tn = []; let n; while ((n = w.nextNode())) tn.push(n);
    tn.forEach(_applyText);
    if (root.querySelectorAll) root.querySelectorAll('[placeholder],[title],[aria-label]').forEach(_applyAttrs);
    if (root.nodeType === 1) _applyAttrs(root);
  } catch (e) { /* nunca romper el render por i18n */ }
}

let _obs = null;
function _observe() { if (_obs && document.body) _obs.observe(document.body, { childList: true, subtree: true }); }
function _startObserver() {
  if (_obs || typeof MutationObserver === 'undefined') return;
  _obs = new MutationObserver((muts) => {
    if (LANG !== 'en') return;          // en español la fuente ya está bien
    _obs.disconnect();
    try { for (const m of muts) { if (m.addedNodes) m.addedNodes.forEach(_autoWalk); } }
    finally { _observe(); }
  });
  _observe();
}


/** Aplica el idioma actual al DOM (marcado con data-i18n / -ph / -title). */
function applyI18n(root) {
  root = root || document;
  root.querySelectorAll('[data-i18n]').forEach(el => {
    const o = _cap(el);
    el.textContent = _pick(o.text);
  });
  root.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const o = _cap(el);
    if (o.ph != null) el.setAttribute('placeholder', _pick(o.ph));
  });
  root.querySelectorAll('[data-i18n-title]').forEach(el => {
    const o = _cap(el);
    if (o.title != null) el.setAttribute('title', _pick(o.title));
    if (o.aria != null) el.setAttribute('aria-label', _pick(o.aria));
  });
  _autoWalk(root);
}

/** Cambia el idioma, persiste, re-aplica al DOM y refresca lo dinámico. */
function setLang(l) {
  LANG = (l === 'en') ? 'en' : 'es';
  try { localStorage.setItem('lang', LANG); } catch (e) {}
  document.documentElement.setAttribute('lang', LANG);
  document.querySelectorAll('[data-lang]').forEach(b =>
    b.classList.toggle('active', b.getAttribute('data-lang') === LANG));
  applyI18n(document);
  _startObserver();
  // Hook que app.js define para re-renderizar vistas dinámicas (menú, nav, tareas…).
  if (typeof window.__afterLang === 'function') {
    try { window.__afterLang(); } catch (e) { console.error('[i18n afterLang]', e); }
  }
}

// Inicialización: fija <html lang>, marca el botón activo y aplica al cargar.
(function initI18n() {
  const run = () => {
    document.documentElement.setAttribute('lang', LANG);
    document.querySelectorAll('[data-lang]').forEach(b =>
      b.classList.toggle('active', b.getAttribute('data-lang') === LANG));
    applyI18n(document);
    _startObserver();
  };
  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', run);
  } else {
    run();
  }
})();

